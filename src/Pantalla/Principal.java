    private String mostrarCondicionFiscal(String condicionFiscal) {
        return switch (condicionFiscal) {
            case "Monotributo" -> "Mono";
            case "Responsable Inscripto" -> "R.I.";
            default -> "C.F.";
        };
}
        private int devolverComboBox(String condicionFiscal) {
        return switch (condicionFiscal) {
            case "C.F." -> 0;
            case "Monotributo"-> 1;
            default -> 2;
        };
}
    
